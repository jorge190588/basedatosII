--ORDER BY
SELECT * FROM CLIENTE ORDER BY NOMBRE DESC;

--WHERE ATRIBUTO
SELECT * FROM CLIENTE WHERE ID = 3;
SELECT * FROM CLIENTE WHERE ID IN(2,3); --CONSULTA UN DETERMINADO NUMEROS DE ID DE TODOS LOS DATOS
SELECT * FROM CLIENTE WHERE ID NOT IN(2,3); --CONSULTA UN DETERMINADO NUMEROS DE ID DE TODOS LOS DATOS SIN INCLUIR EL 2 Y 3
SELECT * FROM CLIENTE WHERE ID > 4; --CONSULTA TODOS LOS REGISTROS MAYORES A 4
SELECT * FROM CLIENTE WHERE ID < 4; --CONSULTA TODOS LOS REGISTROS MENORES A 4
SELECT * FROM CLIENTE WHERE ID >= 4; --CONSULTA TODOS LOS REGISTROS MAYORES E IGUAL A 4
SELECT * FROM CLIENTE WHERE ID <= 4; --CONSULTA TODOS LOS REGISTROS MENORES E IGUAL A 4
SELECT * FROM CLIENTE WHERE ID BETWEEN 2 AND 4; --CONSULTA UN RANGO DE REGISTROS
SELECT * FROM CLIENTE WHERE ID BETWEEN 3 AND 5 OR ID = 1; --CONSULTA UN RANGO DE REGISTROS Y TAMBIEN EL 1
SELECT * FROM CLIENTE WHERE ID >= 3 AND ID <= 5 OR ID = 1;

--UPDATE
