--LIKE
-- CONVERSION DE DATOS UPPER(ATRIBUTO) Y LOWER(ATRIBUTO)
SELECT * FROM CLIENTE WHERE UPPER(DIRECCION) LIKE 'R%'; --BUSCA LA PALABRA QUE INICIA CON R Y POSTERIOMENTE VA A CONTENER CUALQUIER CARACTER ESO SE LO DECIMOS ATRAVES DEL %
SELECT * FROM CLIENTE WHERE DIRECCION LIKE '%TE%'; --BUSCA CUALQUIER PALABRA QUE CONTENGA LA PALABRA TE HAY QUE TENER CONSIDERACIONES LAS MAYUS Y MINUSCULAS
SELECT * FROM CLIENTE WHERE DIRECCION LIKE '%A'; -- BUSCA CUALQUIER VALOR QUE FINALICE CON LA LETRA A

